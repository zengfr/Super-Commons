package com.zengfr.supercommons;
import com.google.common.primitives.UnsignedInteger;
/**
* @author zengfr
* https://github.com/zengfr/Super-Commons
* http://blog.csdn.net/zfrong/
* zengfr3000@qq.com
*/
public final class UnsignedIntegerUtil{ 
public static com.google.common.primitives.UnsignedInteger valueOf(java.lang.String p0,int p1){
return UnsignedInteger.valueOf(p0,p1);
}
public static com.google.common.primitives.UnsignedInteger valueOf(long p0){
return UnsignedInteger.valueOf(p0);
}
public static com.google.common.primitives.UnsignedInteger valueOf(java.lang.String p0){
return UnsignedInteger.valueOf(p0);
}
public static com.google.common.primitives.UnsignedInteger valueOf(java.math.BigInteger p0){
return UnsignedInteger.valueOf(p0);
}
public static com.google.common.primitives.UnsignedInteger fromIntBits(int p0){
return UnsignedInteger.fromIntBits(p0);
}
}
